

/**
 * 
 * @export
 * @interface Definition
 */
export interface Definition {
    /**
     * 
     * @type {number}
     * @memberof Definition
     */
    'id': number;
    /**
     * 
     * @type {string}
     * @memberof Definition
     */
    'name': string;
    /**
     * 
     * @type {string}
     * @memberof Definition
     */
    'description'?: string;
    /**
     * 
     * @type {boolean}
     * @memberof Definition
     */
    'duplicable'?: boolean;
    /**
     * 
     * @type {string}
     * @memberof Definition
     */
    'state': string;
    /**
     * 
     * @type {number}
     * @memberof Definition
     */
    'version': number;
}
